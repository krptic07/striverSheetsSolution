const mergeSort = (arr, low, high) => {
  if (low >= high) {
    return;
  }
  const mid = ~~((low + high) / 2);
  mergeSort(arr, low, mid);
  mergeSort(arr, mid + 1, high);
  merge(arr, low, mid, high);
};

const merge = (arr, low, mid, high) => {
  let temp = [];
  let i = low;
  let j = mid + 1;
  while (i <= mid && j <= high) {
    if (arr[i] < arr[j]) {
      temp.push(arr[i]);
      i++;
    }
    if (arr[j] < arr[i]) {
      temp.push(arr[j]);
      j++;
    }
  }
  while (i <= mid) {
    temp.push(arr[i]);
    i++;
  }
  while (j <= high) {
    temp.push(arr[j]);
    j++;
  }
  for (let k = low; k <= high; k++) {
    arr[k] = temp[k - low];
  }
  console.log(arr);
};

